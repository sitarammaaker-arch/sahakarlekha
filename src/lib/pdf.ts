export function generateDayBookPDF(
  entries: Voucher[],
  accounts: LedgerAccount[],
  society: SocietySettings,
  fromDate: string,
  toDate: string,
  language: 'hi' | 'en'
) {
  const doc = new jsPDF({ orientation: 'landscape' });
  const getAccName = (id: string) => accounts.find(a => a.id === id)?.name || id;
  const typeLabel = (t: string) => t === 'receipt' ? 'Receipt' : t === 'payment' ? 'Payment' : 'Journal';

  const subtitle = `${fmtDate(fromDate)} to ${fmtDate(toDate)} | FY: ${society.financialYear}`;
  const { startY, font } = addHeader(doc, 'Day Book', society, subtitle, { reportCode: 'DB' });

  // Group by date
  const groups: { date: string; items: typeof entries }[] = [];
  entries.forEach(v => {
    const last = groups[groups.length - 1];
    if (last && last.date === v.date) last.items.push(v);
    else groups.push({ date: v.date, items: [v] });
  });

  // Running cash only (Day Book = cash based)
  const cashAccOB = accounts.find(a => a.id === ACCOUNT_IDS.CASH)?.openingBalance || 0;
  // C-2 FIX: Use getVoucherLines() to correctly calculate pre-period cash balance
  // including multi-line Expert Mode vouchers.
  const firstDate = entries.length > 0 ? entries[0].date : null;
  let runCash = cashAccOB;
  if (firstDate) {
    entries.forEach(v => {
      if (v.date >= firstDate) return;
      getVoucherLines(v).forEach(l => {
        if (l.accountId === ACCOUNT_IDS.CASH)
          runCash += l.type === 'Dr' ? l.amount : -l.amount;
      });
    });
  }

  // Columns: Date/Voucher | Particulars | Debit (Rs.) | Credit (Rs.)
  // Each voucher = 2 rows: Dr row + Cr row
  const body: (string[])[] = [];
  const dayHeaderRows: number[] = [];
  const openingBalRows: number[] = [];
  const dayTotalRows: number[] = [];
  const closingBalRows: number[] = [];
  const drRows: number[] = [];
  const crRows: number[] = [];

  groups.forEach(group => {
    const dayDate = fmtDate(group.date);
    const dayTotal = group.items.reduce((s, v) => s + v.amount, 0);
    const dayReceipts = group.items.filter(v => v.type === 'receipt').reduce((s, v) => s + v.amount, 0);
    const dayPayments = group.items.filter(v => v.type === 'payment').reduce((s, v) => s + v.amount, 0);

    // Day header row (spans all 4 cols)
    dayHeaderRows.push(body.length);
    body.push([
      `${dayDate}`,
      `${group.items.length} transaction(s)   Rcpt: ${fmt(dayReceipts)}   Pmnt: ${fmt(dayPayments)}`,
      '',
      `Total: ${fmt(dayTotal)}`,
    ]);

    // Opening balance row
    openingBalRows.push(body.length);
    body.push(['Opening Balance', `Cash in Hand (Opening): ${fmt(runCash)}`, fmt(runCash), '']);

    // C-2 FIX: Use getVoucherLines() for correct Dr/Cr display in multi-line vouchers.
    group.items.forEach((v, i) => {
      const vLines = getVoucherLines(v);
      const narration = v.narration ? `  (${v.narration})` : '';
      const voucherMeta = `${fmtDate(v.date)}\n${typeLabel(v.type)}\n#${i + 1} ${v.voucherNo}`;

      vLines.forEach((l, li) => {
        const label = l.type === 'Dr'
          ? `${getAccName(l.accountId)}  Dr.`
          : `    To ${getAccName(l.accountId)}  Cr.${li === vLines.length - 1 ? narration : ''}`;
        if (l.type === 'Dr') {
          drRows.push(body.length);
          body.push([li === 0 ? voucherMeta : '', label, fmt(l.amount), '']);
        } else {
          crRows.push(body.length);
          body.push(['', label, '', fmt(l.amount)]);
        }
      });
    });

    // Day total row
    dayTotalRows.push(body.length);
    body.push(['Day Total', `${dayDate}`, fmt(dayTotal), fmt(dayTotal)]);

    // C-2 FIX: Update running cash using getVoucherLines() for multi-line voucher support.
    group.items.forEach(v => {
      getVoucherLines(v).forEach(l => {
        if (l.accountId === ACCOUNT_IDS.CASH)
          runCash += l.type === 'Dr' ? l.amount : -l.amount;
      });
    });

    // Closing balance row
    closingBalRows.push(body.length);
    body.push(['Closing Balance', `Cash in Hand (Closing): ${fmt(runCash)}`, fmt(runCash), '']);
  });

  const grandTotal = entries.reduce((s, v) => s + v.amount, 0);

  autoTable(doc, {
    startY,
    head: [['Date / Voucher', 'Particulars', 'Debit (Rs.)', 'Credit (Rs.)']],
    body,
    foot: [['', 'GRAND TOTAL', fmt(grandTotal), fmt(grandTotal)]],
    styles: { fontSize: 8, cellPadding: 2.5 },
    headStyles: { fillColor: [41, 82, 163], textColor: 255, fontStyle: 'bold', fontSize: 9 },
    footStyles: { fillColor: [220, 230, 255], fontStyle: 'bold', textColor: 20, fontSize: 9 },
    columnStyles: {
      0: { cellWidth: 42 },
      1: { cellWidth: 145 },
      2: { cellWidth: 40, halign: 'right' },
      3: { cellWidth: 40, halign: 'right' },
    },
    didParseCell: (data) => {
      if (data.section !== 'body') return;
      const rowIdx = data.row.index;
      if (dayHeaderRows.includes(rowIdx)) {
        data.cell.styles.fillColor = [41, 82, 163];
        data.cell.styles.textColor = [255, 255, 255];
        data.cell.styles.fontStyle = 'bold';
        data.cell.styles.fontSize = 8.5;
      } else if (openingBalRows.includes(rowIdx)) {
        data.cell.styles.fillColor = [255, 248, 220];
        data.cell.styles.fontStyle = 'bold';
        data.cell.styles.textColor = [120, 80, 0];
        if (data.column.index >= 2) data.cell.styles.halign = 'right';
      } else if (drRows.includes(rowIdx)) {
        data.cell.styles.fillColor = [240, 245, 255];
        data.cell.styles.textColor = [30, 50, 150];
        if (data.column.index >= 2) data.cell.styles.halign = 'right';
      } else if (crRows.includes(rowIdx)) {
        data.cell.styles.fillColor = [245, 255, 245];
        data.cell.styles.textColor = [20, 100, 40];
        if (data.column.index >= 2) data.cell.styles.halign = 'right';
      } else if (dayTotalRows.includes(rowIdx)) {
        data.cell.styles.fillColor = [230, 236, 255];
        data.cell.styles.fontStyle = 'bold';
        data.cell.styles.textColor = [30, 30, 150];
        if (data.column.index >= 2) data.cell.styles.halign = 'right';
      } else if (closingBalRows.includes(rowIdx)) {
        data.cell.styles.fillColor = [225, 245, 225];
        data.cell.styles.fontStyle = 'bold';
        data.cell.styles.textColor = [20, 100, 20];
        if (data.column.index >= 2) data.cell.styles.halign = 'right';
      }
    },
  });

  const sigY = (doc as any).lastAutoTable.finalY + 10;
  const sig = getSignatoryNames(society);
  addSignatureBlock(doc, font, ['Accountant', 'Secretary / Manager', 'President'], sigY, undefined,
    [sig.accountant, sig.secretary, sig.president]);

  addPageNumbers(doc, font, society?.name);
  doc.save(pdfFileName('DayBook', society));
}

export function generateTradingAccountPDF(
  data: {
    salesItems:        { name: string; amount: number }[];
    closingStockItems: { name: string; amount: number }[];
    openingStockItems: { name: string; amount: number }[];
    purchaseItems:     { name: string; amount: number }[];
    directExpItems:    { name: string; amount: number }[];
    totalSales: number; totalClosingStock: number;
    totalOpeningStock: number; totalPurchases: number; totalDirectExp: number;
    grossProfit: number;
  },
  society: SocietySettings
) {
  const doc = new jsPDF('landscape');
  const { startY, font } = addHeader(doc, 'Trading Account', society, `Financial Year: ${society.financialYear}`, { reportCode: 'TA' });

  const isProfit  = data.grossProfit >= 0;
  const grandTotal = Math.abs(data.grossProfit) +
    (isProfit
      ? data.totalOpeningStock + data.totalPurchases + data.totalDirectExp
      : data.totalSales + data.totalClosingStock);

  // ── Dr side (left) ────────────────────────────────────────────────────────
  const drBody: (string | { content: string; styles: object })[][] = [];

  if (data.openingStockItems.length > 0) {
    drBody.push([{ content: 'Opening Stock', styles: { fontStyle: 'bold', fillColor: [245, 245, 245] } }, '']);
    data.openingStockItems.forEach(i => drBody.push([`  ${i.name}`, fmt(i.amount)]));
    drBody.push([{ content: 'Total Opening Stock', styles: { fontStyle: 'bold' } }, fmt(data.totalOpeningStock)]);
  }

  if (data.purchaseItems.length > 0) {
    drBody.push([{ content: 'Purchases', styles: { fontStyle: 'bold', fillColor: [245, 245, 245] } }, '']);
    data.purchaseItems.forEach(i => drBody.push([`  ${i.name}`, fmt(i.amount)]));
  }

  if (data.directExpItems.length > 0) {
    drBody.push([{ content: 'Direct Expenses', styles: { fontStyle: 'bold', fillColor: [245, 245, 245] } }, '']);
    data.directExpItems.forEach(i => drBody.push([`  ${i.name}`, fmt(i.amount)]));
  }

  if (isProfit && data.grossProfit > 0) {
    drBody.push([{ content: 'Gross Profit (to I&E Account)', styles: { fontStyle: 'bold', textColor: [25, 135, 84] } }, fmt(data.grossProfit)]);
  }

  // ── Cr side (right) ───────────────────────────────────────────────────────
  const crBody: (string | { content: string; styles: object })[][] = [];

  if (data.salesItems.length > 0) {
    crBody.push([{ content: 'Sales (Trading Income)', styles: { fontStyle: 'bold', fillColor: [245, 245, 245] } }, '']);
    data.salesItems.forEach(i => crBody.push([`  ${i.name}`, fmt(i.amount)]));
    crBody.push([{ content: 'Total Sales', styles: { fontStyle: 'bold' } }, fmt(data.totalSales)]);
  }

  if (data.closingStockItems.length > 0) {
    crBody.push([{ content: 'Closing Stock', styles: { fontStyle: 'bold', fillColor: [245, 245, 245] } }, '']);
    data.closingStockItems.forEach(i => crBody.push([`  ${i.name}`, fmt(i.amount)]));
    crBody.push([{ content: 'Total Closing Stock', styles: { fontStyle: 'bold' } }, fmt(data.totalClosingStock)]);
  }

  if (!isProfit) {
    crBody.push([{ content: 'Gross Loss (to I&E Account)', styles: { fontStyle: 'bold', textColor: [220, 53, 69] } }, fmt(Math.abs(data.grossProfit))]);
  }

  // Render Dr table (left half)
  autoTable(doc, {
    startY,
    margin: { left: 15, right: 155 },
    head: [['Debit (Dr)', 'Amount']],
    body: drBody.length > 0 ? drBody : [['No entries', '']],
    foot: [['Grand Total', fmt(grandTotal)]],
    styles: { fontSize: 8, cellPadding: 2, font },
    headStyles: { fillColor: [220, 53, 69], textColor: 255, fontStyle: 'bold' },
    footStyles: { fillColor: [41, 82, 163], textColor: 255, fontStyle: 'bold' },
    columnStyles: { 1: { halign: 'right', cellWidth: 30 } },
    didParseCell: rightAlignAmountColumns(1),
  });
  const taDrFinalY = (doc as any).lastAutoTable.finalY;

  // Render Cr table (right half)
  autoTable(doc, {
    startY,
    margin: { left: 151, right: 15 },
    head: [['Credit (Cr)', 'Amount']],
    body: crBody.length > 0 ? crBody : [['No entries', '']],
    foot: [['Grand Total', fmt(grandTotal)]],
    styles: { fontSize: 8, cellPadding: 2, font },
    headStyles: { fillColor: [25, 135, 84], textColor: 255, fontStyle: 'bold' },
    footStyles: { fillColor: [41, 82, 163], textColor: 255, fontStyle: 'bold' },
    columnStyles: { 1: { halign: 'right', cellWidth: 30 } },
    didParseCell: rightAlignAmountColumns(1),
  });

  // G8 FIX: Use standardized signature block — use max of both tables to avoid overlap
  const finalY = Math.max(taDrFinalY, (doc as any).lastAutoTable.finalY) + 10;
  const taSig = getSignatoryNames(society);
  addSignatureBlock(doc, font, ['Accountant', 'Secretary', 'Chairman'], finalY,
    'Certified that the above Trading Account has been prepared from the Books of Account of the Society.',
    [taSig.accountant, taSig.secretary, taSig.president]
  );

  addPageNumbers(doc, font, society?.name);
  doc.save(pdfFileName('TradingAccount', society));
}

