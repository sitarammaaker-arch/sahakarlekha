// G1 FIX: Use Rs. prefix — Helvetica font lacks the ₹ glyph, causing garbled output
const fmt = (amount: number): string =>
  'Rs. ' + new Intl.NumberFormat('en-IN', { minimumFractionDigits: 2, maximumFractionDigits: 2 }).format(amount);

const preparedOn = (): string => {
  const d = new Date();
  return d.toLocaleDateString('en-IN', { day: '2-digit', month: 'short', year: 'numeric' });
};

/** All PDFs use English only — helvetica font always. */
function setupFont(_doc: jsPDF): string {
  return 'helvetica';
}

// G5: Generate unique Report ID
function generateReportId(reportCode: string): string {
  const date = new Date().toISOString().slice(0, 10).replace(/-/g, '');
  const rand = Math.random().toString(36).substring(2, 6).toUpperCase();
  return `SL-${reportCode}-${date}-${rand}`;
}

// G12: Look up full state name from code
function getStateName(code: string): string {
  const state = INDIAN_STATES.find(s => s.value === code);
  return state ? state.label : code;
}

// G10: Standardized PDF file naming
export function pdfFileName(reportType: string, society: SocietySettings, fromDate?: string, toDate?: string): string {
  const sanitize = (s: string) => s.replace(/[^a-zA-Z0-9]/g, '_').replace(/_+/g, '_').substring(0, 40);
  const societySlug = sanitize(society.name);
  const fmtDt = (d: string) => d.split('-').reverse().join('-'); // YYYY-MM-DD → DD-MM-YYYY
  if (fromDate && toDate) {
    return `${reportType}_${societySlug}_${fmtDt(fromDate)}_${fmtDt(toDate)}.pdf`;
  }
  return `${reportType}_${societySlug}_FY_${society.financialYear}.pdf`;
}

// G9: Empty data message
export function addNoDataMessage(doc: jsPDF, font: string, y: number): void {
  const pageW = doc.internal.pageSize.width;
  doc.setFontSize(11);
  doc.setFont(font, 'italic');
  doc.setTextColor(150);
  doc.text('No Data Available', pageW / 2, y + 20, { align: 'center' });
  doc.setFontSize(8);
  doc.text('This report has no transactions for the selected period.', pageW / 2, y + 28, { align: 'center' });
  doc.setTextColor(0);
  doc.setFont(font, 'normal');
}

/**
 * Right-align amount columns in head, body AND foot.
 * jspdf-autotable's columnStyles only applies to body by default.
 * This callback forces halign:'right' on specified column indices across all sections.
 */
export function rightAlignAmountColumns(...colIndices: number[]) {
  return (data: any) => {
    if (colIndices.includes(data.column.index)) {
      data.cell.styles.halign = 'right';
    }
  };
}

/** G7 FIX: Add page numbers + confidentiality footer to every page. Call AFTER all content is added. */
export function addPageNumbers(doc: jsPDF, font: string, societyName?: string): void {
  const total = doc.getNumberOfPages();
  const pageW = doc.internal.pageSize.width;
  const pageH = doc.internal.pageSize.height;
  for (let i = 1; i <= total; i++) {
    doc.setPage(i);
    // Thin separator line above footer
    doc.setDrawColor(200);
    doc.setLineWidth(0.2);
    doc.line(15, pageH - 14, pageW - 15, pageH - 14);
    // Footer line 1: Confidentiality (centered)
    if (societyName) {
      doc.setFontSize(6);
      doc.setFont(font, 'normal');
      doc.setTextColor(160);
      doc.text(`Confidential \u2014 For authorized use of ${societyName} only`, pageW / 2, pageH - 10, { align: 'center' });
    }
    // Footer line 2: Generated by (left) | Page X of Y (right)
    doc.setFontSize(7);
    doc.setTextColor(120);
    // Soft viral footer: brand + value prop, as a clickable link on every exported report.
    doc.textWithLink('Generated free with SahakarLekha · sahakarlekha.com', 15, pageH - 6, { url: 'https://sahakarlekha.com' });
    doc.text(`Page ${i} of ${total}`, pageW - 15, pageH - 6, { align: 'right' });
    doc.setTextColor(0);
  }
}

/**
 * Get signatory names from society settings. Falls back to empty strings if not configured.
 */
export function getSignatoryNames(society: SocietySettings): { accountant: string; secretary: string; president: string } {
  const sig = society.signatories || {};
  return {
    accountant: sig.accountant?.name || '',
    secretary: sig.secretary?.name || '',
    president: sig.president?.name || '',
  };
}

/**
 * Add a 3-column signature block with optional signatory names above designation labels.
 * labels: e.g. ['Accountant', 'Secretary', 'President']
 * names: optional array of actual person names to print above the designation
 * Returns the Y position used.
 */
export function addSignatureBlock(doc: jsPDF, font: string, labels: string[], startY: number, note?: string, names?: string[]): number {
  const pageW = doc.internal.pageSize.width;
  const pageH = doc.internal.pageSize.height;
  // If not enough space on current page, add new page
  if (startY + 35 > pageH - 20) {
    doc.addPage();
    startY = 20;
  }
  // Inset signatures with proper margins (40px from each edge)
  const marginIn = 40;
  const positions = labels.map((_, i) => marginIn + (i * (pageW - marginIn * 2) / (labels.length - 1 || 1)));
  doc.setFontSize(8);
  doc.setFont(font, 'normal');
  if (note) {
    doc.setFontSize(7.5);
    doc.setTextColor(80);
    doc.text(note, pageW / 2, startY, { align: 'center' });
    doc.setTextColor(0);
    startY += 6;
  }
  labels.forEach((lbl, i) => {
    const x = labels.length === 1 ? pageW / 2 : positions[i];
    const lblLines = lbl.split('\n').length;  // count lines in label (e.g., "Secretary\n(Seal)" = 2)
    const extraGap = (lblLines - 1) * 4;     // 4px per extra line
    doc.setDrawColor(80);
    doc.setLineWidth(0.3);
    doc.line(x - 25, startY + 8, x + 25, startY + 8);
    // Print actual name above designation (if configured)
    const name = names?.[i];
    if (name) {
      doc.setFontSize(8);
      doc.setFont(font, 'bold');
      doc.text(name, x, startY + 13, { align: 'center' });
      doc.setFont(font, 'normal');
      doc.setFontSize(7.5);
      doc.text(lbl, x, startY + 18, { align: 'center' });
      doc.setFontSize(7);
      doc.setTextColor(100);
      doc.text('Date: __________', x, startY + 24 + extraGap, { align: 'center' });
    } else {
      doc.setFontSize(7.5);
      doc.text(lbl, x, startY + 13, { align: 'center' });
      doc.setFontSize(7);
      doc.setTextColor(100);
      doc.text('Date: __________', x, startY + 19 + extraGap, { align: 'center' });
    }
    doc.setTextColor(0);
  });
  return startY + 26;
}

/**
 * Auditor's Certificate block — per Cooperative Societies Act requirements.
 * Printed at bottom of Balance Sheet / I&E Account.
 */
function addAuditorCertificate(doc: jsPDF, font: string, society: SocietySettings, reportName: string, startY: number): void {
  const pageW = doc.internal.pageSize.width;
  const pageH = doc.internal.pageSize.height;
  if (startY + 50 > pageH - 15) {
    doc.addPage();
    startY = 15;
  }
  doc.setDrawColor(41, 82, 163);
  doc.setLineWidth(0.3);
  doc.line(15, startY, pageW - 15, startY);
  startY += 5;

  doc.setFontSize(8);
  doc.setFont(font, 'bold');
  doc.text("AUDITOR'S CERTIFICATE", pageW / 2, startY, { align: 'center' });
  startY += 5;

  doc.setFont(font, 'normal');
  doc.setFontSize(7.5);
  // G13 FIX: Use state-specific act name instead of hardcoded Haryana
  const stateFormat = getStateAuditFormat(society.state || 'hr');
  const certText =
    `We have examined the ${reportName} of ${society.name} (Reg. No. ${society.registrationNo}) ` +
    `for the Financial Year ${society.financialYear} and certify that the same is drawn up in accordance ` +
    `with the provisions of the ${stateFormat.actName} and the rules made thereunder ` +
    `and presents a true and fair view of the state of affairs of the Society as on 31st March.`;
  const lines = doc.splitTextToSize(certText, pageW - 30);
  doc.text(lines, 15, startY);
  startY += lines.length * 4 + 4;

  const certSig = getSignatoryNames(society);
  addSignatureBlock(doc, font,
    ['Statutory Auditor\n(Name & Membership No.)', 'Secretary\n(Seal)', 'President\n(Seal)'],
    startY, undefined,
    ['', certSig.secretary, certSig.president]  // Auditor name left blank (filled manually)
  );
}

/** Enhanced addHeader — society info, GSTIN, Report ID, Generated By, state full name. */
export function addHeader(
  doc: jsPDF, title: string, society: SocietySettings, subtitle?: string,
  options?: { reportCode?: string; generatedBy?: string }
): { startY: number; font: string; reportId: string } {
  const font = setupFont(doc);
  const pageW = doc.internal.pageSize.width;
  const cx = pageW / 2;
  const marginR = pageW - 15;

  // Society name
  doc.setFontSize(14);
  doc.setFont(font, 'bold');
  doc.text(society.name, cx, 15, { align: 'center' });

  // Reg No + FY (center)
  doc.setFontSize(8);
  doc.setFont(font, 'normal');
  const regLine = `Reg. No: ${society.registrationNo} | FY: ${society.financialYear}`;
  doc.text(regLine, cx, 21, { align: 'center' });
  doc.setTextColor(0);

  // Address line — G12: use full state name
  const stateName = society.state ? getStateName(society.state) : '';
  const addrParts = [society.address, society.district, stateName].filter(Boolean);
  if (society.pinCode) addrParts.push(`PIN: ${society.pinCode}`);
  if (addrParts.length > 0) {
    doc.setFontSize(7.5);
    doc.setTextColor(60);
    doc.text(addrParts.join(', '), cx, 26, { align: 'center' });
    doc.setTextColor(0);
  }

  // G2: GSTIN / PAN line
  const taxIds: string[] = [];
  if ((society as any).gstin) taxIds.push(`GSTIN: ${(society as any).gstin}`);
  if (society.entityPan) taxIds.push(`PAN: ${society.entityPan}`);
  if (society.tan) taxIds.push(`TAN: ${society.tan}`);
  if (taxIds.length > 0) {
    doc.setFontSize(7);
    doc.setTextColor(80);
    doc.text(taxIds.join('  |  '), cx, 30, { align: 'center' });
    doc.setTextColor(0);
  }
  const taxIdOffset = taxIds.length > 0 ? 4 : 0;

  // Report title
  doc.setFontSize(13);
  doc.setFont(font, 'bold');
  doc.text(title, cx, 33 + taxIdOffset, { align: 'center' });

  if (subtitle) {
    doc.setFontSize(8.5);
    doc.setFont(font, 'normal');
    doc.text(subtitle, cx, 39 + taxIdOffset, { align: 'center' });
  }

  // Report ID (left) + Prepared on (right) — same line, just above separator
  const metaY = 39 + taxIdOffset;
  doc.setFontSize(6.5);
  doc.setTextColor(130);
  const reportId = options?.reportCode ? generateReportId(options.reportCode) : '';
  if (reportId) {
    doc.text(`Report ID: ${reportId}`, 15, metaY);
  }
  doc.text(`Prepared on: ${preparedOn()}`, marginR, metaY, { align: 'right' });
  doc.setTextColor(0);

  doc.setDrawColor(41, 82, 163);
  doc.setLineWidth(0.5);
  const lineY = 42 + taxIdOffset;
  doc.line(15, lineY, marginR, lineY);
  return { startY: lineY + 4, font, reportId };
}

export function generateCashBookPDF(
  entries: CashBookEntry[],
  society: SocietySettings,
  openingBalance: number,
  language: 'hi' | 'en'
) {
  const doc = new jsPDF();
  const { startY, font } = addHeader(doc, 'Cash Book', society, `Financial Year: ${society.financialYear}`, { reportCode: 'CB' });

  // G9: Empty data guard
  if (entries.length === 0) {
    addNoDataMessage(doc, font, startY);
    addPageNumbers(doc, font, society?.name);
    doc.save(pdfFileName('CashBook', society));
    return;
  }

  const totalReceipts = entries.filter(e => e.type === 'receipt').reduce((s, e) => s + e.amount, 0);
  const totalPayments = entries.filter(e => e.type === 'payment').reduce((s, e) => s + e.amount, 0);
  const closingBalance = entries.length > 0 ? entries[entries.length - 1].runningBalance : openingBalance;

  const body = [
    ['', 'OB', 'Opening Balance', fmt(openingBalance), '', fmt(openingBalance)],
    ...entries.map(e => [
      fmtDate(e.date),
      e.voucherNo,
      e.particulars,
      e.type === 'receipt' ? fmt(e.amount) : '',
      e.type === 'payment' ? fmt(e.amount) : '',
      fmt(e.runningBalance),
    ]),
  ];

  autoTable(doc, {
    startY,
    head: [['Date', 'Voucher No.', 'Particulars', 'Receipt (Dr)', 'Payment (Cr)', 'Balance']],
    body,
    foot: [['', '', 'Total', fmt(totalReceipts), fmt(totalPayments), fmt(closingBalance)]],
    styles: { fontSize: 8, cellPadding: 2, font },
    headStyles: { fillColor: [41, 82, 163], textColor: 255, fontStyle: 'bold' },
    footStyles: { fillColor: [41, 82, 163], textColor: 255, fontStyle: 'bold' },
    alternateRowStyles: { fillColor: [248, 250, 253] },
    columnStyles: { 3: { halign: 'right', cellWidth: 28 }, 4: { halign: 'right', cellWidth: 28 }, 5: { halign: 'right', cellWidth: 28 } },
    didParseCell: rightAlignAmountColumns(3, 4, 5),
  });

  const sigY = (doc as any).lastAutoTable.finalY + 10;
  const sig = getSignatoryNames(society);
  addSignatureBlock(doc, font, ['Accountant', 'Secretary / Manager', 'President'], sigY, undefined,
    [sig.accountant, sig.secretary, sig.president]);

  addPageNumbers(doc, font, society?.name);
  doc.save(pdfFileName('CashBook', society));
}

